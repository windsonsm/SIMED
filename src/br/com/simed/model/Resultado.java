package br.com.simed.model;
public class Resultado extends Exame{
    private int codigoResultado;
    private String nomeResultado;

    public int getCodigoResultado() {
        return codigoResultado;
    }

    public void setCodigoResultado(int codigoResultado) {
        this.codigoResultado = codigoResultado;
    }

    public String getNomeResultado() {
        return nomeResultado;
    }

    public void setNomeResultado(String nomeResultado) {
        this.nomeResultado = nomeResultado;
    }
    
    
}
